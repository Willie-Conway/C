// Declaring (Creating) Variables
// To create a variable, you must specify the type and assign it a value


#include <iostream>
using namespace std;

int main() {
  int myNum = 15;   // Now myNum is 15
  myNum = 10;       // Now myNum is 10
  cout << myNum;   // Outputs 10
  return 0;
}
